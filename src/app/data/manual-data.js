async function downloadChampions() {
  console.log("🚀 Scaricamento dati da Riot Games...");
  const url = "https://ddragon.leagueoflegends.com/cdn/14.3.1/data/it_IT/champion.json";

  try {
    const response = await fetch(url);
    const data = await response.json();
    
    const rawChamps = Object.values(data.data).map(champ => ({
      id: champ.id,
      name: champ.name,
      // Usiamo la funzione aggiornata sotto per i ruoli corretti (Top, Mid, ecc.)
      role: getMapRole(champ.tags, champ.id),
      image: `https://ddragon.leagueoflegends.com/cdn/14.3.1/img/champion/${champ.image.full}`,
    }));

    // Funzione per generare i counter
    function autoCountersFor(champ) {
      if (manualData[champ.id]) return manualData[champ.id];

      // Logica semplificata: se il nemico è un Marksman (ADC), suggerisci dei Middle (Assassini)
      // Se è un Tank, suggerisci dei Bottom (ADC con danno continuo)
      const suggestions = [];
      if (champ.role === "Bottom") suggestions.push(...rawChamps.filter(c => c.role === "Middle").slice(0, 3));
      if (champ.role === "Top") suggestions.push(...rawChamps.filter(c => c.role === "Top").reverse().slice(0, 3));
      
      return suggestions.map(c => c.name).slice(0, 3);
    }

    const championsList = rawChamps.map(champ => ({
      ...champ,
      counters: autoCountersFor(champ)
    }));

    const content = `export const champions = ${JSON.stringify(championsList, null, 2)};`;
    
    // CONTROLLA BENE QUESTO PERCORSO: deve corrispondere a dove hai la cartella data
    fs.writeFileSync('./src/app/data/champions.ts', content); 

    console.log(`✅ Successo!`);
  } catch (error) {
    console.error("❌ Errore:", error);
  }
}

function getMapRole(tags, id) {
  if (tags.includes("Tank")) return "Top";
  if (tags.includes("Mage") || tags.includes("Assassin")) return "Middle";
  if (tags.includes("Support")) return "Support";
  if (tags.includes("Marksman")) return "Bottom";
  return "Jungle";
}

